     * @return string
     * @throws \Exception
     */
    public static function continentCodeToContinent($continentCode)
    {
        switch ($continentCode) {
            case 'EU':
                return 'Europe';

            case 'NA':
                return 'North America';

            case 'SA':
                return 'South America';

            case 'AF':
                return 'Africa';

            case 'AN':
                return 'Antarctica';

            case 'OC':
                return 'Oceania';

            case 'AS':
                return 'Asia';

            default:
                return null;
        }
    }

    /**
     * @param $countryCode
     * @return mixed|null
     */
    public static function countryCodeToCountry($countryCode)
    {
        return isset(self::$countries[$countryCode]) ? self::$countries[$countryCode] : null;
    }

    /**
     * @param $countryCode
     * @return mixed
     */
    public static function countryCodeToTimezone($countryCode)
    {
        $timezones = \DateTimeZone::listIdentifiers(\DateTimeZone::PER_COUNTRY, $countryCode);
        $timezone = array_shift($timezones);

        return $timezone;
    }

    /**
     * @param $timezone
     * @return string
     */
    public static function timezoneToTimezoneOffset($timezone)
    {
        try {
            $time = new \DateTime('now', new \DateTimeZone($timezone));
            $timezoneOffset = $time->format('P');
            return $timezoneOffset;
        } catch (\Exception $e) {
            return null;
        }
    }
